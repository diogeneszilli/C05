int		ft_is_prime(int nb)
{
	int n;

	n = 2;
	if (nb <= 1)
		return (0);
	while (n <= nb)
	{
		if ((n != nb) && nb % n == 0)
			return (0);
		n++;
	}
	return (1);
}
